#[allow(unused_variables)]
#[allow(dead_code)]

fn main() {
    // using if let vs match
    // using match here would get into a highly branched code
    let favorite_color: Option<&str> = None;
    let is_tuesday = false;
    let age: Result<u8, _> = "34".parse();

    if let Some(color) = favorite_color {
        println!("Using your favorite color, {}, as the background", color);
    } else if is_tuesday {
        println!("Tuesday is green day!");
    } else if let Ok(age) = age {
        if age > 30 {
            println!("Using purple as the background color");
        } else {
            println!("Using orange as the background color");
        }
    } else {
        println!("Using blue as the background color");
    }

    // using while let
    let mut stack = Vec::new();

    stack.push(1);
    stack.push(2);
    stack.push(3);

    while let Some(top) = stack.pop() {
        println!("{}", top);
    }

    // destructuring tuples
    let v = vec!['a', 'b', 'c'];

    for (index, value) in v.iter().enumerate() {
        println!("{} is at index {}", value, index);
    }

    // a simple pattern: using let
    let pattern = "expression";
    let (x, y, z) = (1, 2, 3);
    // doesn't work: let (x, y) = (1, 2, 3);
    // (this will be discussed in further section)

    // function parameters are also patterns
    fn foo(pattern: i32) {
        // code goes here
    }
    fn print_coordinates(&(x, y): &(i32, i32)) {
        println!("Current location: ({}, {})", x, y);
    }
    let point = (3, 5);
    print_coordinates(&point);
}
